// ── Key Metrics fundamentals grid ──────────────────────────────────
// Four cards (Valuation / Profitability / Financial Health / Growth)
// rendered above ResearchAssistant. Null-safe — every value defaults
// to em-dash. Number formatting per spec:
//   Market Cap: ₹{(market_cap / 1e7).toFixed(0)} Cr  (Indian locale
//               separators added so "11,37,000 Cr" not "1137000 Cr")
//   Ratios:     2 decimal places
//   Percentages: stored as decimal fractions in DB (0.138 = 13.8%);
//                multiplied by 100 here for display
function KeyMetricsGrid({ keyMetrics, priceHistory }) {
  // Render the four cards even when the key_metrics row is missing — the
  // grid structure stays visible so users can see what the section will
  // contain once the fundamentals fetcher catches up. Every cell falls
  // back to em-dash via the null-safe formatters below.
  const k = keyMetrics || {}
  const dash = '—'
  const num2 = (v) => (v == null || !Number.isFinite(Number(v)) ? dash : Number(v).toFixed(2))
  const num1 = (v) => (v == null || !Number.isFinite(Number(v)) ? dash : Number(v).toFixed(1))
  const pct1 = (v) => (v == null || !Number.isFinite(Number(v)) ? dash : `${(Number(v) * 100).toFixed(1)}%`)
  const cr = (v) => (
    v == null || !Number.isFinite(Number(v))
      ? dash
      : `₹${Math.round(Number(v) / 1e7).toLocaleString('en-IN')} Cr`
  )
  const rupee = (v) => (
    v == null || !Number.isFinite(Number(v))
      ? dash
      : `₹${Number(v).toLocaleString('en-IN', { maximumFractionDigits: 2 })}`
  )

  const groups = [
    {
      title: 'Valuation',
      rows: [
        ['Market Cap', cr(k.market_cap)],
        ['PE Ratio',   num1(k.pe_ratio)],
        ['Forward PE', num1(k.forward_pe)],
        ['PS Ratio',   num2(k.ps_ratio ?? k.price_to_sales)],
        ['PB Ratio',   num2(k.pb_ratio)],
        ['EV/EBITDA',  num2(k.ev_ebitda)],
      ],
    },
    {
      title: 'Profitability',
      rows: [
        ['ROE',                pct1(k.roe)],
        ['ROA',                pct1(k.roa)],
        ['ROCE',               pct1(k.roce)],
        ['Profit Margin',      pct1(k.profit_margins)],
        ['Operating Margin',   pct1(k.operating_margins)],
        ['EPS TTM',            num2(k.eps_ttm)],
      ],
    },
    {
      title: 'Financial Health',
      rows: [
        ['D/E Ratio',     num2(k.de_ratio)],
        ['Current Ratio', num2(k.current_ratio)],
        ['Beta',          num2(k.beta)],
      ],
    },
    {
      title: 'Growth',
      rows: [
        ['Revenue Growth',  pct1(k.revenue_growth)],
        ['Earnings Growth', pct1(k.earnings_growth)],
        ['52W High',        rupee(k.fifty_two_week_high)],
        ['52W Low',         rupee(k.fifty_two_week_low)],
        ['Dividend Yield',  pct1(k.dividend_yield)],
      ],
    },
  ]

  return (
    <div style={{ marginTop: 20 }}>
      <div
        style={{
          fontSize: 11,
          color: 'var(--text-muted)',
          textTransform: 'uppercase',
          letterSpacing: '0.06em',
          fontWeight: 600,
          marginBottom: 10,
        }}
      >
        Key Metrics
      </div>
      <div
        style={{
          display: 'grid',
          gridTemplateColumns: 'repeat(auto-fit, minmax(220px, 1fr))',
          gap: 12,
        }}
      >
        {groups.map((g) => {
          // Empty-state gate: when every row in the group resolves to
          // the em-dash placeholder, the underlying fundamentals row
          // hasn't been ingested yet (or this column family is still
          // mid-backfill). Replace the row list with a calm muted
          // message instead of a wall of dashes.
          const hasAnyValue = g.rows.some(([, value]) => value !== dash)
          return (
            <div
              key={g.title}
              style={{
                background: 'var(--bg-surface)',
                border: '1px solid var(--border)',
                borderRadius: 12,
                padding: '14px 16px',
              }}
            >
              <div
                style={{
                  fontSize: 11,
                  fontWeight: 700,
                  color: 'var(--text-primary)',
                  marginBottom: 10,
                  letterSpacing: '0.02em',
                }}
              >
                {g.title}
              </div>
              {hasAnyValue ? (
                <div style={{ display: 'grid', rowGap: 6 }}>
                  {g.rows.map(([label, value]) => (
                    <div
                      key={label}
                      style={{
                        display: 'flex',
                        justifyContent: 'space-between',
                        alignItems: 'baseline',
                        gap: 10,
                        fontSize: 12,
                      }}
                    >
                      <span style={{ color: 'var(--text-muted)' }}>{label}</span>
                      <span className="num" style={{ color: 'var(--text-primary)', fontWeight: 600 }}>
                        {value}
                      </span>
                    </div>
                  ))}
                </div>
              ) : (
                <div
                  style={{
                    fontSize: 12,
                    color: 'var(--text-muted)',
                    lineHeight: 1.5,
                    fontStyle: 'italic',
                  }}
                >
                  Financial data loading. Available after data pipeline completes.
                </div>
              )}
            </div>
          )
        })}
      </div>
    </div>
  )
}

