/**
 * StockDetail — "Quiet Clarity" redesign.
 *
 * The page is intentionally narrative-first. It reads as a cycle
 * essay, not a dashboard. Five fixed-title accordions surface the
 * Gemini-authored description on demand; the fifth accordion is a
 * HARDCODED stoploss philosophy answer (same on every stock) — never
 * AI-generated. A Pro gate accordion sits below the cycle content.
 *
 * Hard rules encoded here:
 *   - NO raw price numbers anywhere on the page.
 *   - Banned words in the narrative: buy / sell / invest / target /
 *     stoploss / support / resistance. We trust Gemini upstream not
 *     to produce these, but a runtime check logs a warning to the
 *     console if any slip through.
 *   - The SEBI disclaimer footer is ALWAYS rendered, no conditional.
 *   - Animation is spring(300, 35) — controlled, never bouncy.
 */
import { useEffect, useState, lazy, Suspense } from 'react'
import { useNavigate, useParams } from 'react-router-dom'
import { Helmet } from 'react-helmet-async'
import { motion, AnimatePresence } from 'framer-motion'
import { ArrowLeft, Plus, Lock } from 'lucide-react'
import { C } from '../styles/tokens'
import { useAuth } from '../context'
import { supabase } from '../lib/supabase'
import {
  insertWatchlistRow,
  selectWatchMembership,
  deleteWatchlistRow,
} from '../lib/watchlistTable'
import Skeleton from '../components/ui/Skeleton'
// Code-split ResearchAssistant — it's ~50 KB (framer-motion logic +
// per-category prompt builders + multi-turn state) and renders below
// the fold on first paint. Lazy-loading shrinks the StockDetail entry
// chunk and shaves ~150ms off the parse cost on slower CPUs.
const ResearchAssistant = lazy(() =>
  import('../components/ResearchAssistant'),
)

// ── Constants ──────────────────────────────────────────────────────

// Map description.phase → reader-facing phase label. The five values
// below are the only labels rendered in the hero; anything else falls
// through to a quiet em-dash so we don't leak Gemini noise into the UI.
const PHASE_LABELS = {
  advancing: 'Advancing',
  basing: 'Basing',
  topping: 'Topping',
  declining: 'Declining',
}

// Words the cycle narrative MUST NOT contain. Upstream Gemini is
// instructed to avoid them; this list is the runtime safety net.
const BANNED_NARRATIVE_WORDS = [
  'buy',
  'sell',
  'invest',
  'target',
  'stoploss',
  'support',
  'resistance',
]

// Hardcoded answer for the fifth accordion. Identical for every
// stock — this is a philosophy statement, not stock-specific advice,
// and is deliberately NOT generated by Gemini.
const STOPLOSS_ANSWER = `We don't show stoplosses on PineX. A stoploss is a personal risk decision — it depends on your position size, your timeframe and the cycle phase you're trading. Two people looking at the same stock will reasonably choose different exits. What we do show is the cycle picture: phase, criteria, trend and participation. The exit level is yours to set.`

// Accordion titles 1–4 read from these description fields. Title 5
// is hardcoded above. Order matters — this is the reading sequence.
const CYCLE_ACCORDIONS = [
  { title: "What's happening in this stock right now?",    field: 'whats_happening' },
  { title: 'Why is this phase what it is?',                field: 'why_this_phase' },
  { title: 'What changes the picture?',                    field: 'what_changes' },
  { title: 'How does this fit the broader cycle?',         field: 'broader_cycle' },
]

const SPRING = { type: 'spring', stiffness: 300, damping: 35 }

// ── Local Accordion ────────────────────────────────────────────────

function Accordion({ title, body, isProGate = false }) {
  const [open, setOpen] = useState(false)
  return (
    <div
      style={{
        background: C.surface,
        border: `1px solid ${C.border}`,
        borderRadius: 12,
        overflow: 'hidden',
      }}
    >
      <button
        type="button"
        onClick={() => setOpen((o) => !o)}
        aria-expanded={open}
        className="w-full flex items-center justify-between text-left"
        style={{
          padding: '14px 18px',
          background: 'transparent',
          border: 'none',
          cursor: 'pointer',
          color: C.text,
          fontFamily: 'Newsreader, ui-serif, Georgia, serif',
          fontSize: '1rem',
          fontWeight: 500,
          letterSpacing: '-0.01em',
        }}
      >
        <span style={{ display: 'flex', alignItems: 'center', gap: 10 }}>
          {isProGate && <Lock size={14} style={{ color: C.amber }} />}
          {title}
        </span>
        <span
          aria-hidden="true"
          style={{
            color: C.textMuted,
            fontSize: 18,
            transform: open ? 'rotate(45deg)' : 'rotate(0deg)',
            transition: 'transform 200ms ease',
            display: 'inline-flex',
          }}
        >
          <Plus size={16} />
        </span>
      </button>
      <AnimatePresence initial={false}>
        {open && (
          <motion.div
            key="body"
            initial={{ height: 0, opacity: 0 }}
            animate={{ height: 'auto', opacity: 1 }}
            exit={{ height: 0, opacity: 0 }}
            transition={SPRING}
            style={{ overflow: 'hidden' }}
          >
            <div
              style={{
                padding: '4px 18px 18px',
                color: C.textMuted,
                fontFamily: 'Newsreader, ui-serif, Georgia, serif',
                fontSize: '1rem',
                lineHeight: 1.75,
                borderTop: `1px solid ${C.border}`,
                paddingTop: 14,
              }}
            >
              {body}
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  )
}

