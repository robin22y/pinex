// ── Module-level promise cache ─────────────────────────────────────
// Two perf wins in one Map:
//   1. React.StrictMode in dev double-invokes every useEffect. Without
//      this cache the main 4-way fetch fires TWICE on every mount in
//      dev — exactly the "slow localhost" Robin reported.
//   2. Back/forward navigation within the SPA session returns the
//      cached promise instead of refetching — instant transitions.
// Cache key is the uppercase symbol. Value is the in-flight Promise
// (also the resolved value once settled — same reference, no race).
// On fetch error we delete the entry so a retry can try again.
// We never invalidate on success: stock-detail data refreshes daily
// post-EOD, fine for a session lifetime. Hard-refresh forces re-pull.
const stockPageCache = new Map()

// Narrow select for stock_descriptions — only the fields the page
// actually renders. The earlier `.select('*')` pulled the entire row
// including the raw Gemini response, system prompt, and token-usage
// metadata (kilobytes per row). We render 14 named fields total.
const DESCRIPTION_FIELDS = [
  'phase',
  'narrative',
  'malayalam_line',
  'criteria_score',
  'days_in_phase',
  'sector',
  'sector_breadth_pct',
  'whats_happening',
  'why_this_phase',
  'what_changes',
  'broader_cycle',
].join(',')

function loadStockPageData(rawSym) {
  if (!rawSym) return Promise.resolve(null)
  const key = String(rawSym).toUpperCase()
  if (stockPageCache.has(key)) return stockPageCache.get(key)

  // ── TWO-PHASE FETCH — company first, the rest in parallel ──────
  // The previous shape fired everything in one Promise.all using the
  // companies!inner(symbol) embed on swing_conditions and price_data.
  // That embed forces Postgres to plan a join from the giant
  // price_data table through companies on every page view — and the
  // planner intermittently picks a path slow enough to hit the
  // statement timeout ("canceling statement due to statement
  // timeout"), leaving the page with empty price history (no phase
  // pill, no RS chip, no Key Metrics) while everything else loaded.
  //
  // New shape:
  //   Phase 1 — companies by symbol (indexed, ~30 ms) → company_id.
  //   Phase 2 — stock_descriptions (by symbol — has its own column),
  //             swing_conditions + price_data by company_id, all in
  //             parallel. company_id+date is the native index on both
  //             tables, so the planner takes the fast path every time.
  // Net: one extra serial round-trip (~50 ms) buys away an
  // intermittent multi-second timeout. Worth it everywhere.
  const promise = (async () => {
    const c = await supabase
      .from('companies')
      // stage_override + reason power the manual-correction display:
      // when set, the page renders the override label instead of the
      // pipeline stage and shows a "Manually reviewed" badge.
      .select('id, symbol, name, sector, market_cap, stage_override, stage_override_reason')
      .eq('symbol', key)
      .maybeSingle()
    const company = c?.data ?? null
    const cid = company?.id || null

    const descP = supabase
      .from('stock_descriptions')
      .select(DESCRIPTION_FIELDS)
      .eq('symbol', key)
      .order('trading_date', { ascending: false })
      .limit(1)
      .maybeSingle()

    // condP — limit(60) powers BOTH the most-recent conditions panel
    // (consumer takes [0]) and the 60-day CriteriaChart series.
    // Per-condition booleans feed the Key Metrics strip dots.
    // condition_delivery_above_avg deliberately NOT selected: the
    // backend hardcodes it false since delivery was dropped from the
    // SwingX score, so a dot for it would be dead UI.
    const condP = cid
      ? supabase
          .from('swing_conditions')
          // stage_override + override_note carry both auto-corrected
          // Stage 3 → Stage 1 fixes from the pipeline AND admin
          // temporary overrides. StockDetail prefers the permanent
          // companies.stage_override first, falls back to this row's
          // override, then to the calculated description.phase.
          .select('conditions_met, date, criteria_change_reason, condition_stage2, condition_near_ma50, condition_rsi_healthy, condition_volume_contracting, stage_override, override_note')
          .eq('company_id', cid)
          .order('date', { ascending: false })
          .limit(60)
      : Promise.resolve({ data: [] })

    // priceHistP — 120 rows (~6 mo) for the Phase Duration counter.
    // rs_vs_nifty feeds the header RS chip; rsi feeds the Key Metrics
    // strip + the momentum gauge category. close + ma30w + high_52w +
    // low_52w are fetched ONLY for derived percentages (distance to
    // the 30W trend, 52-week range position) — none of the raw rupee
    // values is ever rendered.
    const priceHistP = cid
      ? supabase
          .from('price_data')
          // weinstein_substage + vol_ratio added for PatternHistory's
          // matcher — both already populated by the daily price-data
          // pipeline, just not previously selected here.
          // prev_close + price_change_1d added for the header's
          // 1-day change line (₹/% delta vs yesterday). price_change_1d
          // is often NULL on newer pipeline rows so prev_close acts as
          // the fallback the IIFE in the header uses.
          .select('date, stage, weinstein_substage, rs_vs_nifty, mansfield_rs, rsi, close, prev_close, price_change_1d, ma50, ma30w, vol_ratio, high_52w, low_52w')
          .eq('company_id', cid)
          .order('date', { ascending: false })
          .limit(120)
      : Promise.resolve({ data: [] })

    // key_metrics — fundamentals for the Key Metrics grid above the
    // Research Assistant. Indexed by symbol (not company_id) per the
    // pipeline schema. maybeSingle so a missing row is null, not an
    // error, on stocks the fundamentals fetcher hasn't covered yet.
    const keyMetricsP = supabase
      .from('key_metrics')
      .select('*')
      .eq('symbol', key)
      .maybeSingle()

    const [d, s, ph, km] = await Promise.all([descP, condP, priceHistP, keyMetricsP])
    const condRows = Array.isArray(s?.data) ? s.data : []
    return {
      description:        d?.data ?? null,
      company,
      conditions:         condRows[0] ?? null,
      conditionsHistory:  condRows,
      priceHistory:       Array.isArray(ph?.data) ? ph.data : [],
      keyMetrics:         km?.data ?? null,
    }
  })().catch((err) => {
    stockPageCache.delete(key)
    throw err
  })

  stockPageCache.set(key, promise)
  return promise
}


