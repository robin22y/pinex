// ── Component ──────────────────────────────────────────────────────

export default function StockDetail() {
  const { symbol } = useParams()
  const navigate = useNavigate()
  const { user } = useAuth()

  const sym = symbol?.toUpperCase()

  // Data state — `null` until first load resolves. We never render
  // raw prices, so we don't store price rows here.
  const [description, setDescription] = useState(null) // stock_descriptions row | null
  const [company, setCompany]         = useState(null) // companies row | null
  const [conditions, setConditions]   = useState(null) // swing_conditions row | null
  // criteria_changes row for THIS symbol on TODAY's trading date,
  // when one exists. The pipeline writes this row only when at least
  // one condition flips vs the prior trading day, so a null here just
  // means "nothing changed today" — not an error state.
  const [criteriaChange, setCriteriaChange] = useState(null)
  const [loading, setLoading]         = useState(true)

  // Watchlist state — same shape as the legacy file.
  const [watching, setWatching]           = useState(false)
  const [watchlistRowId, setWatchlistRowId] = useState(null)
  const [watchLoading, setWatchLoading]   = useState(false)
  const [watchError, setWatchError]       = useState(null)

  // ── Fetch on mount (parallel) ────────────────────────────────────
  useEffect(() => {
    if (!sym) return
    let cancelled = false
    setLoading(true)

    const descP = supabase
      .from('stock_descriptions')
      .select('*')
      .eq('symbol', sym)
      .order('trading_date', { ascending: false })
      .limit(1)
      .maybeSingle()

    const compP = supabase
      .from('companies')
      .select('id, symbol, name, sector, market_cap')
      .eq('symbol', sym)
      .maybeSingle()

    // swing_conditions was migrated to `company_id` + `date` (no
    // `symbol`, no `trading_date`). We use PostgREST's inner-embed
    // pattern + filter on the joined companies.symbol so we can
    // still resolve the latest row without first round-tripping for
    // company.id. The previous query silently returned null for
    // every stock — criteriaScore was always null.
    const condP = supabase
      .from('swing_conditions')
      .select('conditions_met, date, companies!inner(symbol)')
      .eq('companies.symbol', sym)
      .order('date', { ascending: false })
      .limit(1)
      .maybeSingle()

    // criteria_changes — fetch the most-recent row for this symbol
    // (capped at the last 7 days). If the newest row's trading_date
    // matches today's NSE trading day we render the "Changed today:
    // <reason>" line below the criteria dots. We don't filter by
    // .eq('trading_date', today) because the browser's "today" can
    // race the NSE close (and on weekends the latest trading day is
    // yesterday or Friday). Picking the newest-and-most-recent row,
    // then doing the date comparison client-side, is more forgiving.
    const changeP = supabase
      .from('criteria_changes')
      .select('trading_date, criteria_change_reason, gained, lost')
      .eq('symbol', sym)
      .order('trading_date', { ascending: false })
      .limit(1)
      .maybeSingle()

    Promise.all([descP, compP, condP, changeP]).then(([d, c, s, ch]) => {
      if (cancelled) return
      setDescription(d?.data ?? null)
      setCompany(c?.data ?? null)
      setConditions(s?.data ?? null)
      setCriteriaChange(ch?.data ?? null)
      setLoading(false)

      // Banned-word check on the narrative. Console-only — never
      // shown to the user. Catches Gemini drift early.
      const narrative = d?.data?.narrative
      if (typeof narrative === 'string') {
        const lower = narrative.toLowerCase()
        const hit = BANNED_NARRATIVE_WORDS.find((w) =>
          new RegExp(`\\b${w}\\b`, 'i').test(lower),
        )
        if (hit) {
          // eslint-disable-next-line no-console
          console.warn(
            `[StockDetail] narrative for ${sym} contains banned word "${hit}". Upstream Gemini drift — please review.`,
          )
        }
      }
    })

    return () => { cancelled = true }
  }, [sym])

  // ── Watchlist membership check (mirrors legacy) ─────────────────
  useEffect(() => {
    if (!user?.id || !company?.id) {
      setWatching(false)
      setWatchlistRowId(null)
      return
    }
    let cancelled = false
    selectWatchMembership(user.id, company.id).then(({ data }) => {
      if (cancelled) return
      if (data?.id) {
        setWatching(true)
        setWatchlistRowId(data.id)
      } else {
        setWatching(false)
        setWatchlistRowId(null)
      }
    })
    return () => { cancelled = true }
  }, [user?.id, company?.id])

  // ── Watchlist toggle — EXACT logic from the legacy file ─────────
  function humanizeWatchError(err) {
    const code = err?.code
    const msg = err?.message || ''
    if (code === '42501' || msg.includes('row-level security')) {
      return 'Permission denied — please sign in again.'
    }
    if (code === '23505') {
      return 'Already in your watchlist.'
    }
    if (msg.toLowerCase().includes('jwt') || msg.includes('401')) {
      return 'Session expired — please sign in again.'
    }
    return 'Could not update watchlist. Please try again.'
  }

  const handleWatchToggle = async () => {
    if (!user) {
      navigate('/login', { state: { next: `/stock/${sym}` } })
      return
    }
    if (watchLoading) return
    if (!company?.id) {
      setWatchError('Stock data is still loading — please try again in a moment.')
      return
    }
    setWatchLoading(true)
    setWatchError(null)
    try {
      if (watching && watchlistRowId) {
        const { error: delErr } = await deleteWatchlistRow(user.id, watchlistRowId)
        if (delErr) {
          setWatchError(humanizeWatchError(delErr))
          return
        }
        setWatching(false)
        setWatchlistRowId(null)
      } else {
        // Note: legacy passes `price_at_add: price?.close ?? null`.
        // Quiet Clarity never reads price into component state, so we
        // omit it — the column is nullable and the row still saves.
        const { data, error } = await insertWatchlistRow({
          user_id: user.id,
          company_id: company.id,
          symbol: sym,
          group_name: 'My Watchlist',
          added_at: new Date().toISOString(),
          price_at_add: null,
        })
        if (error) {
          setWatchError(humanizeWatchError(error))
          return
        }
        setWatching(true)
        setWatchlistRowId(data?.id ?? null)
      }
    } catch (e) {
      setWatchError(humanizeWatchError(e))
    } finally {
      setWatchLoading(false)
    }
  }

  // ── Derived display values ──────────────────────────────────────
  const phaseRaw = description?.phase
  const phaseLabel = phaseRaw ? (PHASE_LABELS[String(phaseRaw).toLowerCase()] || null) : null
  const streak = description?.phase_streak_days ?? description?.streak_days ?? null
  const malayalam = description?.malayalam_line || description?.tagline_malayalam || null
  const narrative = description?.narrative || null
  const criteriaScore = conditions?.conditions_met ?? null

  // ── Render ───────────────────────────────────────────────────────
  return (
    <>
      <Helmet>
        <title>{company?.name || sym || symbol} — PineX</title>
        <meta
          name="description"
          content={
            narrative?.slice(0, 160) ||
            `Cycle analysis for ${sym || symbol} on PineX`
          }
        />
      </Helmet>

      <div
        style={{
          minHeight: '100vh',
          background: C.base,
          color: C.text,
          fontFamily: 'system-ui, -apple-system, sans-serif',
        }}
      >
        <motion.div
          initial={{ opacity: 0, y: 8 }}
          animate={{ opacity: 1, y: 0 }}
          transition={SPRING}
          className="mx-auto"
          style={{ maxWidth: 720, padding: '24px 20px 48px' }}
        >
          {/* ── HERO ─────────────────────────────────────────── */}
          <div className="flex items-center justify-between" style={{ marginBottom: 24 }}>
            <button
              type="button"
              onClick={() => navigate(-1)}
              className="inline-flex items-center"
              style={{
                gap: 6,
                background: 'transparent',
                border: 'none',
                color: C.textMuted,
                fontSize: 13,
                cursor: 'pointer',
                padding: '6px 0',
              }}
            >
              <ArrowLeft size={14} />
              <span>Back</span>
            </button>
            <button
              type="button"
              onClick={handleWatchToggle}
              disabled={watchLoading}
              style={{
                background: watching ? C.accentBg : 'transparent',
                color: watching ? C.amber : C.textMuted,
                border: `1px solid ${watching ? C.amber : C.border}`,
                borderRadius: 999,
                padding: '6px 14px',
                fontSize: 12,
                fontWeight: 600,
                cursor: watchLoading ? 'wait' : 'pointer',
                letterSpacing: '0.02em',
              }}
            >
              {watchLoading
                ? 'Saving…'
                : watching
                ? 'On your watchlist'
                : '+ Watchlist'}
            </button>
          </div>

          {loading ? (
            <div style={{ display: 'flex', flexDirection: 'column', gap: 14 }}>
              <Skeleton height={32} width="60%" />
              <Skeleton height={20} width="40%" />
              <Skeleton height={120} />
            </div>
          ) : (
            <>
              {/* Company name + phase */}
              <h1
                style={{
                  margin: 0,
                  fontFamily: 'Newsreader, ui-serif, Georgia, serif',
                  fontSize: '2rem',
                  fontWeight: 500,
                  letterSpacing: '-0.02em',
                  color: C.textHeading,
                  lineHeight: 1.15,
                }}
              >
                {company?.name || sym}
              </h1>
              <div
                style={{
                  display: 'flex',
                  alignItems: 'center',
                  gap: 10,
                  flexWrap: 'wrap',
                  marginTop: 10,
                  marginBottom: 6,
                }}
              >
                <span
                  style={{
                    color: C.textMuted,
                    fontSize: 13,
                    letterSpacing: '0.03em',
                  }}
                >
                  {sym}
                </span>
                {phaseLabel && (
                  <span
                    style={{
                      background: C.accentBg,
                      color: C.amber,
                      border: `1px solid ${C.accentMuted}`,
                      borderRadius: 999,
                      padding: '2px 10px',
                      fontSize: 11,
                      fontWeight: 700,
                      letterSpacing: '0.06em',
                      textTransform: 'uppercase',
                    }}
                  >
                    {phaseLabel}
                  </span>
                )}
                {streak != null && (
                  <span style={{ color: C.textFaint, fontSize: 12 }}>
                    {streak} days {'🔥'}
                  </span>
                )}
              </div>

              {/* Malayalam line */}
              {malayalam && (
                <p
                  style={{
                    margin: '6px 0 0',
                    color: C.textMuted,
                    fontFamily: 'Newsreader, ui-serif, Georgia, serif',
                    fontStyle: 'italic',
                    fontSize: '1rem',
                    letterSpacing: '-0.005em',
                  }}
                >
                  {malayalam}
                </p>
              )}

              {watchError && (
                <p
                  style={{
                    marginTop: 10,
                    color: C.red,
                    fontSize: 12,
                  }}
                >
                  {watchError}
                </p>
              )}

              {/* ── NARRATIVE ────────────────────────────────── */}
              <div style={{ marginTop: 32 }}>
                {narrative ? (
                  <>
                    <p
                      style={{
                        margin: 0,
                        color: C.text,
                        fontFamily: 'Newsreader, ui-serif, Georgia, serif',
                        fontSize: '1.05rem',
                        lineHeight: 1.7,
                        letterSpacing: '-0.005em',
                      }}
                    >
                      {narrative}
                    </p>

                    {/* 5 criteria dots — one dot per criterion. Lit
                        dots = criteria met. No labels, no numbers
                        beyond the dot pattern. */}
                    <div
                      style={{
                        display: 'flex',
                        gap: 8,
                        marginTop: 18,
                        alignItems: 'center',
                      }}
                      aria-label={
                        criteriaScore != null
                          ? `${criteriaScore} of 5 criteria met`
                          : 'Criteria score unavailable'
                      }
                    >
                      {[0, 1, 2, 3, 4].map((i) => {
                        const lit = criteriaScore != null && i < Number(criteriaScore)
                        return (
                          <span
                            key={i}
                            style={{
                              width: 8,
                              height: 8,
                              borderRadius: 999,
                              background: lit ? C.amber : C.surface2,
                              border: `1px solid ${lit ? C.amber : C.border}`,
                              display: 'inline-block',
                            }}
                          />
                        )
                      })}
                      <span
                        style={{
                          marginLeft: 6,
                          fontSize: 11,
                          color: C.textFaint,
                          letterSpacing: '0.04em',
                          textTransform: 'uppercase',
                        }}
                      >
                        {criteriaScore != null ? `${criteriaScore}/5` : '—/5'}
                      </span>
                    </div>

                    {/* "Changed today" line — only renders when the
                        criteria_changes row's trading_date matches the
                        latest swing_conditions date (the same date the
                        criteria dots above reflect). Lines up with the
                        pipeline's daily run, not the browser's wall
                        clock — which keeps the line correct on weekends
                        / holidays when the displayed criteria are from
                        Friday's close.

                        Reason text is data-classification language
                        ("Price moved above 30-week trend line" etc.) —
                        the pipeline never writes prescriptive copy. */}
                    {(() => {
                      const changeDate = criteriaChange?.trading_date
                      const condDate = conditions?.date
                      if (!changeDate || !condDate || changeDate !== condDate) return null
                      const reason = criteriaChange?.criteria_change_reason
                      if (!reason) return null
                      return (
                        <div
                          style={{
                            marginTop: 8,
                            fontSize: 12,
                            color: C.amber,
                            lineHeight: 1.5,
                          }}
                        >
                          Changed today: {reason}
                        </div>
                      )
                    })()}
                  </>
                ) : (
                  <p
                    style={{
                      textAlign: 'center',
                      color: C.textMuted,
                      fontFamily: 'Newsreader, ui-serif, Georgia, serif',
                      fontSize: '1rem',
                      lineHeight: 1.7,
                      padding: '24px 12px',
                    }}
                  >
                    Analysis for this stock is being prepared. Check back
                    after market close today.
                  </p>
                )}
              </div>

              {/* ── Research Assistant (BYOK Gemini) — Pro feature.
                  Self-gates render: shows the no-key teaser when the
                  user is Pro-eligible but hasn't pasted a key, the
                  active panel when both Pro + key are present. The
                  Pro check uses usePlan().canAccess() but with
                  OPEN_FREE=true everyone is treated as Pro right now
                  — primes for paywall launch.
                  Moved above the accordion list so the higher-value
                  open-ended question entry point is the first thing a
                  user sees after the narrative. */}
              {/* Reserved-height wrapper so the lazy-load swap-in
                  doesn't push the accordion list down (CLS). 500px
                  matches the rendered footprint of the 7-tile menu +
                  header + footer on mobile, so when the real component
                  arrives, no visible shift. */}
              <div style={{ marginTop: 28, minHeight: 500 }}>
                <Suspense
                  fallback={
                    <div
                      aria-hidden
                      style={{
                        height: 500,
                        borderRadius: 14,
                        background:
                          'linear-gradient(180deg, rgba(245,159,11,0.04) 0%, rgba(245,159,11,0) 100%)',
                        border: '1px solid rgba(245,159,11,0.10)',
                      }}
                    />
                  }
                >
                  <ResearchAssistant
                    symbol={sym}
                    companyName={company?.name}
                    phase={description?.phase}
                    criteriaScore={description?.criteria_score ?? conditions?.conditions_met}
                    daysInPhase={description?.days_in_phase}
                    sector={company?.sector || description?.sector}
                    sectorBreadth={description?.sector_breadth_pct}
                    narrative={description?.narrative}
                    pctFromMA={conditions?.pct_from_ma}
                    userId={user?.id}
                  />
                </Suspense>
              </div>

              {/* ── ACCORDIONS 1–5 ──────────────────────────── */}
              <div
                style={{
                  marginTop: 32,
                  display: 'flex',
                  flexDirection: 'column',
                  gap: 10,
                }}
              >
                {CYCLE_ACCORDIONS.map((a) => {
                  const body = description?.[a.field]
                  return (
                    <Accordion
                      key={a.field}
                      title={a.title}
                      body={
                        body || (
                          <span style={{ color: C.textFaint }}>
                            Not available for this stock yet.
                          </span>
                        )
                      }
                    />
                  )
                })}
                {/* Fifth accordion — HARDCODED stoploss answer. */}
                <Accordion
                  title="Where should I set my stoploss?"
                  body={STOPLOSS_ANSWER}
                />
              </div>

              {/* ── PRO GATE ────────────────────────────────── */}
              <div style={{ marginTop: 28 }}>
                <Accordion
                  title="Show me similar stocks"
                  isProGate={true}
                  body={<ProGateContent onClick={() => navigate('/pricing')} />}
                />
              </div>
            </>
          )}

          {/* ── SEBI footer — always rendered, no exceptions ── */}
          <SebiFooter />
        </motion.div>
      </div>
    </>
  )
}
